#This program is designed to make you feel old.

print(' Hello! Nice to meet you! What is your name?')
myName = input()

print('Nice to meet you ' + myName + '. Boy, you look old! Like, really REALLY old. How old are you?')
myAge = input()
myDays = int(myAge) * 365

myDiff = myDays - 1

print('Wow, that is pretty old! You are ' + str(myDiff) + ' days older than me! I am only one day old!') 

